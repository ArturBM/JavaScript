function Calculadora(num1, num2){
    const soma = num1 + num2
    console.log("A soma do",num1," + ",num2," = ", soma)

    const sub = num1 - num2
    console.log(`A subtração do ${num1} - ${num2} = ${sub}`)

    const multi = num1 * num2 
    console.log(`A multiplicação do ${num1} X ${num2} = ${multi}`)
   
}

Calculadora(1,2)
Calculadora(10,10)